#' Função Colar
#'
#' Esta função faz o Paste/Colar de duas variáveis recebidas
#'
#' @param a primeiro parâmetro da função
#' @param b segund parâmetro da função
#'
#' @return retorna o resultado do Paste das duas variáveis recebidas
#'
#' @export
colar <- function(a = NULL , b = NULL) {
  paste(a, b)
}
